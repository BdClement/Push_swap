#include "libft.h"

int	ft_atoi(const char *nptr)
{
	long int	value;
	int			sign;

	value = 0;
	sign = 1;
	while (*nptr == ' ' || (*nptr >= 9 && *nptr <= 13))
		nptr++;
	if (*nptr == '-' || *nptr == '+')
	{
		if (*nptr == '-')
			sign = -1;
		nptr++;
	}
	while (*nptr >= '0' && *nptr <= '9')
	{
		value = value * 10 + (*nptr - 48);
		nptr++;
	}
	return ((int)value * sign);
}
